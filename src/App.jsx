/* ───────────────────────────────────────────
   Job Title Picker Component
   ─────────────────────────────────────────── */

function JobTitlePicker({ value, onChange }) {
  const [selectedLetter, setSelectedLetter] = useState('')
  const groupedTitles = useMemo(() => groupJobTitlesByLetter(), [])

  const letters = Array.from({ length: 26 }, (_, i) => String.fromCharCode(65 + i))
  const currentTitles = selectedLetter ? groupedTitles[selectedLetter] : []

  return (
    <div className="job-title-picker">
      <div className="letter-selector">
        <div className="letter-buttons">
          {letters.map((letter) => (
            <button
              key={letter}
              className={selectedLetter === letter ? 'letter-btn active' : 'letter-btn'}
              onClick={() => setSelectedLetter(letter)}
            >
              {letter}
            </button>
          ))}
        </div>
        {selectedLetter && (
          <div className="job-title-list-container">
            <label>Job titles starting with {selectedLetter}</label>
            <select value={value} onChange={(e) => onChange(e.target.value)} className="job-title-dropdown">
              <option value="">Select a job title</option>
              {currentTitles.map((title) => (
                <option key={title} value={title}>
                  {title}
                </option>
              ))}
            </select>
          </div>
        )}
        {value && (
          <div className="selected-job-title">
            <strong>Selected:</strong> {value}
          </div>
        )}
      </div>
    </div>
  )
}

