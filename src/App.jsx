/* Helper function to group job titles by first letter */
function groupJobTitlesByLetter() {
  const grouped = {}
  for (let i = 65; i <= 90; i++) {
    grouped[String.fromCharCode(i)] = []
  }
  jobTitlesList.forEach((title) => {
    const firstLetter = title.charAt(0).toUpperCase()
    if (grouped[firstLetter]) {
      grouped[firstLetter].push(title)
    }
  })
  return grouped
}

